class CWAttack(AttackBase):
    """Wrapper for the Carlini-Wagner attacks from Carlini (L2/L∞/L0).

    Expected model interface (Carlini repo style):
      - model.predict(x_tensor): returns logits (pre-softmax) for x_tensor
      - model.image_size, model.num_channels, model.num_labels

    Targets:
      - For targeted CW: pass y as one-hot targets (N, K)
      - For untargeted CW: pass y as one-hot *true labels* and set targeted=False when creating the attacker
    """

    def __init__(
        self,
        sess: Any,
        model: Any,
        norm: Norm = "l2",
        targeted: bool = True,
        cw_kwargs: Optional[Dict[str, Any]] = None,
    ):
        if tf is None:
            raise RuntimeError("TensorFlow is not available in this environment.")
        self.sess = sess
        self.model = model
        self.norm = norm
        self.targeted = targeted
        self.cw_kwargs = cw_kwargs or {}

        # Import lazily so this file can be used without the CW files if needed
        if norm == "l2":
            from l2_attack import CarliniL2  # noqa: F401
            self._attacker = CarliniL2(sess, model, targeted=targeted, **self.cw_kwargs)
        elif norm == "linf":
            from li_attack import CarliniLi  # noqa: F401
            self._attacker = CarliniLi(sess, model, targeted=targeted, **self.cw_kwargs)
        elif norm == "l0":
            from l0_attack import CarliniL0  # noqa: F401
            self._attacker = CarliniL0(sess, model, targeted=targeted, **self.cw_kwargs)
        else:
            raise ValueError(f"Unsupported norm for CWAttack: {norm}")

    def generate(self, x: np.ndarray, y: np.ndarray, sess: Optional[Any] = None) -> np.ndarray:
        # sess kept for unified signature; CW attacker already owns the session
        x = _ensure_numpy(x).astype(np.float32)
        y = _ensure_numpy(y).astype(np.float32)
        return self._attacker.attack(x, y)


