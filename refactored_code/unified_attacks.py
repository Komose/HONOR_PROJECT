"""
unified_attacks.py

A small "unified API" layer that lets you call multiple adversarial attacks
through a consistent interface, while still reusing your existing codebases.

- PGD: TF1 graph-gradient (expects a TF1-style model with x_input/y_input/logits)
- C&W: wraps the Carlini-Wagner implementations (L2 / L∞ / L0) from Carlini (2016/2017)

Designed so you can later add FGSM / DeepFool with minimal changes.

Author: (generated by ChatGPT)
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any, Dict, Literal, Optional, Union

import numpy as np

try:
    import tensorflow as tf  # TF1.x / TF2 in compat.v1
except Exception as e:  # pragma: no cover
    tf = None  # type: ignore


Norm = Literal["linf", "l2", "l0"]


# -----------------------------
# Common config + small helpers
# -----------------------------

@dataclass
class AttackConfig:
    """Common attack configuration.

    Notes
    -----
    - clip_min/clip_max are the *valid input range* in the same scale as x.
      For CIFAR in your PGD code you use [0, 255]. In many ML repos it's [0, 1]
      or [-0.5, 0.5]. Keep it consistent with the model's expected preprocessing.
    """
    norm: Norm = "linf"

    # Constraint / step params
    epsilon: float = 8.0
    num_steps: int = 10
    step_size: float = 2.0
    random_start: bool = True

    # Input range clamp (after every step)
    clip_min: float = 0.0
    clip_max: float = 255.0

    # Loss / objective options
    loss: Literal["xent", "cw_margin"] = "xent"
    cw_kappa: float = 50.0  # margin used in your PGD "cw" style loss


def _ensure_numpy(x: Any) -> np.ndarray:
    if isinstance(x, np.ndarray):
        return x
    return np.asarray(x)


def _is_onehot(y: np.ndarray) -> bool:
    return y.ndim == 2 and np.all((y == 0) | (y == 1)) and np.allclose(y.sum(axis=1), 1.0)


def to_onehot(y: Union[np.ndarray, list], num_classes: int) -> np.ndarray:
    y = _ensure_numpy(y)
    if _is_onehot(y):
        return y.astype(np.float32)
    if y.ndim != 1:
        raise ValueError(f"Expected label vector shape (N,), got {y.shape}")
    oh = np.zeros((y.shape[0], num_classes), dtype=np.float32)
    oh[np.arange(y.shape[0]), y.astype(int)] = 1.0
    return oh


