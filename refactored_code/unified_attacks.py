class PGDLinfTF1Attack(AttackBase):
    """PGD under L∞ constraint (TF1 graph gradients).

    Expected model interface (TF1-style):
      - model.x_input: tf.placeholder/tensor, shape [None, H, W, C]
      - model.y_input: tf.placeholder/tensor, shape [None] (int labels) or [None, K] (one-hot)
      - model.pre_softmax: logits tensor for model.x_input, shape [None, K]
      - model.xent: cross-entropy loss tensor (can be sum over batch)

    This mirrors the LinfPGDAttack class you pasted, but exposes a unified generate() API.
    """

    def __init__(self, model: Any, config: AttackConfig, num_classes: int = 10):
        if tf is None:
            raise RuntimeError("TensorFlow is not available in this environment.")
        self.model = model
        self.cfg = config
        self.num_classes = num_classes

        # Build loss op (graph)
        self._loss = self._build_loss_op()
        self._grad = tf.gradients(self._loss, self.model.x_input)[0]

    def _build_loss_op(self):
        # Default: use model.xent if present (matches your snippet)
        if self.cfg.loss == "xent":
            if hasattr(self.model, "xent"):
                return self.model.xent
            # fallback: build xent from logits
            logits = getattr(self.model, "pre_softmax")
            y_in = self.model.y_input
            if y_in.dtype.is_integer:
                y_oh = tf.one_hot(y_in, self.num_classes, dtype=tf.float32)
            else:
                y_oh = tf.cast(y_in, tf.float32)
            return tf.reduce_sum(tf.nn.softmax_cross_entropy_with_logits(logits=logits, labels=y_oh))

        # CW-style margin loss used in your PGD code ("cw")
        logits = getattr(self.model, "pre_softmax")
        y_in = self.model.y_input

        if y_in.dtype.is_integer:
            y_oh = tf.one_hot(y_in, self.num_classes, dtype=tf.float32)
        else:
            y_oh = tf.cast(y_in, tf.float32)

        correct_logit = tf.reduce_sum(y_oh * logits, axis=1)
        wrong_logit = tf.reduce_max((1 - y_oh) * logits - 1e4 * y_oh, axis=1)
        # identical structure to your code; kappa controlled by cfg.cw_kappa
        loss_vec = -tf.nn.relu(correct_logit - wrong_logit + self.cfg.cw_kappa)
        return tf.reduce_sum(loss_vec)

    def generate(self, x: np.ndarray, y: np.ndarray, sess: Optional[Any] = None) -> np.ndarray:
        if sess is None:
            raise ValueError("PGDLinfTF1Attack requires an active tf.Session passed as sess=...")

        x_nat = _ensure_numpy(x).astype(np.float32)
        y_in = _ensure_numpy(y)

        # Random start in the epsilon-ball
        if self.cfg.random_start:
            x_adv = x_nat + np.random.uniform(-self.cfg.epsilon, self.cfg.epsilon, x_nat.shape).astype(np.float32)
            x_adv = np.clip(x_adv, self.cfg.clip_min, self.cfg.clip_max)
        else:
            x_adv = x_nat.copy()

        # Iterative updates
        for _ in range(int(self.cfg.num_steps)):
            grad = sess.run(
                self._grad,
                feed_dict={self.model.x_input: x_adv, self.model.y_input: y_in},
            )
            x_adv = x_adv + self.cfg.step_size * np.sign(grad).astype(np.float32)

            # Project back to Linf ball
            x_adv = np.clip(x_adv, x_nat - self.cfg.epsilon, x_nat + self.cfg.epsilon)
            # Clamp to valid input range
            x_adv = np.clip(x_adv, self.cfg.clip_min, self.cfg.clip_max)

        return x_adv


