def make_attack(
    name: Literal["pgd", "cw"],
    *,
    model: Any,
    sess: Optional[Any] = None,
    config: Optional[AttackConfig] = None,
    cw_norm: Norm = "l2",
    cw_targeted: bool = True,
    cw_kwargs: Optional[Dict[str, Any]] = None,
) -> AttackBase:
    """Create an attack with a consistent call signature."""
    if name == "pgd":
        if config is None:
            config = AttackConfig(norm="linf")
        if config.norm != "linf":
            raise ValueError("This PGD wrapper currently implements only Linf. Extend if needed.")
        return PGDLinfTF1Attack(model=model, config=config)
    elif name == "cw":
        if sess is None:
            raise ValueError("CWAttack requires a tf.Session passed as sess=...")
        return CWAttack(sess=sess, model=model, norm=cw_norm, targeted=cw_targeted, cw_kwargs=cw_kwargs)
    else:
        raise ValueError(f"Unknown attack name: {name}")


